a = input('Qual seu nome? : ')
print('O tipo primitivo desse valor é: ' ,(type(a)))
print('Está em diminutivo? : ',a.islower())
print('O valor é alfabeto númerico? : ',a.isalnum())
print('O valor é alfabético ?: ',a.isalpha())
print('Verdade que não possui acento neste valor? : ',a.isascii())
print('A resposta se baseia em numeros? : ',a.isdigit())
print('Esta em maiusculo a palavra? : ',a.isupper())
print('Esta em minusculo a palavra? : ',a.islower())
print('A resposta é um valor decimal ? ', a.isdecimal())
print('Esta capitalizada? Toda frase começa com letra maiscula? : ',a.istitle())
print('Á resposta é imprimivel ?',a.isprintable())



